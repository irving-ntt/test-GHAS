-- QUERY PARA GENERAR ARCHIVO 65 (EN CASO DE QUE NO CONVIVA NINGUNA CUENTA) 
SELECT 
-- C.DETALLE,
RPAD(C.DETALLE,128) AS DETALLE
FROM 
(
SELECT 
'01'||TO_CHAR(SYSDATE,'YYYYMMDD')||'000000000' DETALLE FROM DUAL
UNION
SELECT                                                                                                              
'09'||TO_CHAR(SYSDATE,'YYYYMMDD')||'000000000000000000000000000' DETALLE FROM DUAL
) C