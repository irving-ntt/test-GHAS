SELECT
'09' AS TIPO_REGISTRO,
LPAD((TOTAL_REGISTRO:: STRING),9,'0') AS TOTAL_REGISTRO,
FILLER,
SUM_SALDO97,
FILLER6
FROM
(
SELECT
FTC_FOLIO_BITACORA,
RPAD(' ',45,' ') AS FILLER,
RPAD('0',15,'0') AS SUM_SALDO97,
RPAD(' ',659,' ') AS FILLER6,
SUM(REG) as TOTAL_REGISTRO
FROM
(
SELECT
FTC_FOLIO_BITACORA,
1 AS REG
FROM #DELTA_SUMARIO_SOLMARC#
--dbx_mit_dev_1udbvf_workspace.default.delta_0699_arc_rec_lin2_tf_200_campos_202507171410392809
)
GROUP BY FTC_FOLIO_BITACORA
)