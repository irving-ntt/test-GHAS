SUBSTR(AE.FTC_LINEA,17,8) FTD_FEH_CRE_LOTE,
SUBSTR(AE.FTC_LINEA,28,8) FTD_FEH_LIM_RES,
TO_NUMBER(SUBSTR(AE.FTC_LINEA,25,3)) FTN_CONSEC_DIA,
