FROM CIERREN_ETL.TTSISGRAL_ETL_LEE_ARCHIVO  ASM
JOIN CIERREN_ETL.TTSISGRAL_ETL_LEE_ARCHIVO  AE ON ASM.FTN_ID_ARCHIVO=AE.FTN_ID_ARCHIVO
WHERE ASM.FTN_ID_ARCHIVO = #sr_id_archivo#
AND SUBSTR(ASM.FTC_LINEA,1,2) = '09'
AND SUBSTR(AE.FTC_LINEA,1,2) = '01'