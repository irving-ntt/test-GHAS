      VIV.FTN_DETALLE,
      VIV.FTD_FEH_REG
    FROM #DELTA_VAL_IDENT# MCA
    LEFT JOIN #DELTA_VIV# VIV
      ON MCA.FTN_NUM_CTA_INVDUAL = VIV.FTN_NUM_CTA_INVDUAL
    ORDER BY MCA.FTN_NSS, MCA.FTN_CONTA_SERV
) DER
LEFT JOIN 
(  SELECT 
      BNDDUP, FTC_NSS_INFONA, FTN_CONTA_SERV  --FTN_NSS
    FROM #DELTA_DUP#
    ORDER BY FTC_NSS_INFONA, FTN_CONTA_SERV    --FTN_NSS
) IZQ
ON  DER.FTN_NSS = IZQ.FTC_NSS_INFONA  --FTN_NSS
AND DER.FTN_CONTA_SERV = IZQ.FTN_CONTA_SERV
