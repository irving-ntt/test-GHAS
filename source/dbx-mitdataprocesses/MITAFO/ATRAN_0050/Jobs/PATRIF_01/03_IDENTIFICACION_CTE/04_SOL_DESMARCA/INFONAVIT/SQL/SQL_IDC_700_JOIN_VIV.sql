  DER.FTC_RFC_INFONA,
  DER.FTC_APELLIDO_PATER_INFONA,
  DER.FTC_APELLIDO_MATER_INFONA,
  DER.FTC_NOMBRE_INFONA,
  DER.FTC_IDENTI_LOTE_SOLIC,
  DER.FTN_NSS ,
  DER.FTC_RFC_AFORE,
  DER.FTC_APELLIDO_PATER_AFORE,
  DER.FTC_APELLIDO_MATER_AFORE,
  DER.FTC_NOMBRE_AFORE,
  DER.FTN_SALDO_97,
  DER.FTN_SALDO_92,
  DER.FTC_CODIGO_RES_OPER,
  DER.FTC_DIAGNOS_PROCE,
  DER.FTC_NOMBRE_IMSS,
