  --DER.FTC_ID_DIAGNOSTICO,
  --DER.FCN_ID_IND_CTA_INDV,
  --DER.FFN_ID_CONFIG_INDI, 
  --DER.FTC_VIGENCIA,
  --DER.FTN_DETALLE,
  IZQ.BNDDUP
FROM (
    SELECT
      MCA.FTN_ESTATUS_DIAG,
      MCA.FTD_FEH_PRESEN,
      MCA.FTC_CURP,
      MCA.FTC_NSS_INFONA ,
      MCA.FTN_CTA_AFORE,
      MCA.FTC_RFC_INFONA,
      MCA.FTC_APELLIDO_PATER_INFONA,
      MCA.FTC_APELLIDO_MATER_INFONA,
      MCA.FTC_NOMBRE_INFONA,
